// ClaudeCodeAdapter handles Claude Code (Anthropic's coding assistant).
// Claude Code uses CLAUDE.md file in project root.
// Similar to Antigravity but with Claude-specific format.
type ClaudeCodeAdapter struct{}

// Name returns the adapter name
func (c *ClaudeCodeAdapter) Name() string {
	return "ClaudeCode"
}

// Detect checks if this is a Claude Code project.
// Look for CLAUDE.md file.
func (c *ClaudeCodeAdapter) Detect(projectPath string) bool {
	claudeFile := filepath.Join(projectPath, "CLAUDE.md")
	if _, err := os.Stat(claudeFile); err == nil {
		return true
	}
	claudeLower := filepath.Join(projectPath, "claude.md")
	if _, err := os.Stat(claudeLower); err == nil {
		return true
	}
	return false
}

// Install creates Claude Code configuration from templates.
func (c *ClaudeCodeAdapter) Install(tmpl *templates.Templates, opts InstallOptions) error {
	claudeFile := filepath.Join(opts.TargetDir, "CLAUDE.md")

	if _, err := os.Stat(claudeFile); err == nil && !opts.Force {
		if opts.DryRun {
			return nil
		}
		return fmt.Errorf("claude file %s already exists (use --force to overwrite)", claudeFile)
	}

	content := c.buildContent(tmpl)

	if opts.DryRun {
		return nil
	}

	return os.WriteFile(claudeFile, []byte(content), 0644)
}

// buildContent creates the CLAUDE.md content
func (c *ClaudeCodeAdapter) buildContent(tmpl *templates.Templates) string {
	var sb strings.Builder

	sb.WriteString("# CLAUDE.md - Generated by AGEN\n\n")
	sb.WriteString("> This file provides context for Claude Code.\n")
	sb.WriteString("> Generated by AGEN (https://github.com/eshanized/agen)\n\n")
	sb.WriteString("---\n\n")

	// Project context
	sb.WriteString("## Project Overview\n\n")
	sb.WriteString("This is an AGEN-managed project with defined agents, skills, and workflows.\n\n")

	// Code standards
	sb.WriteString("## Code Standards\n\n")
	sb.WriteString("- Follow clean code principles (SRP, DRY, KISS)\n")
	sb.WriteString("- Use meaningful names that reveal intent\n")
	sb.WriteString("- Keep functions small (ideally < 20 lines)\n")
	sb.WriteString("- Write comprehensive tests\n")
	sb.WriteString("- Document public APIs\n\n")

	// Agents
	sb.WriteString("## Available Agents\n\n")
	sb.WriteString("Use these personas for specialized tasks:\n\n")
	for name, agent := range tmpl.Agents {
		sb.WriteString("### ")
		sb.WriteString(name)
		sb.WriteString("\n")
		sb.WriteString(agent.Description)
		sb.WriteString("\n\n")
	}

	// Skills
	sb.WriteString("## Skills Reference\n\n")
	for name, skill := range tmpl.Skills {
		sb.WriteString("- **")
		sb.WriteString(name)
		sb.WriteString("**: ")
		sb.WriteString(skill.Description)
		sb.WriteString("\n")
	}
	sb.WriteString("\n")

	// Workflows
	sb.WriteString("## Workflow Commands\n\n")
	sb.WriteString("These commands trigger specific workflows:\n\n")
	for name, workflow := range tmpl.Workflows {
		displayName := name
		if !strings.HasPrefix(name, "/") {
			displayName = "/" + name
		}
		sb.WriteString("- `")
		sb.WriteString(displayName)
		sb.WriteString("`: ")
		sb.WriteString(workflow.Description)
		sb.WriteString("\n")
	}

	return sb.String()
}

// Update updates Claude Code configuration
func (c *ClaudeCodeAdapter) Update(tmpl *templates.Templates, opts UpdateOptions) (*UpdateChanges, error) {
	changes := &UpdateChanges{}
	claudeFile := filepath.Join(opts.TargetDir, "CLAUDE.md")

	if _, err := os.Stat(claudeFile); os.IsNotExist(err) {
		if err := c.Install(tmpl, InstallOptions{
			TargetDir: opts.TargetDir,
			DryRun:    opts.DryRun,
			Force:     true,
		}); err != nil {
			return nil, err
		}
		changes.Added = append(changes.Added, "CLAUDE.md")
	} else {
		if opts.Force || opts.DryRun {
			if err := c.Install(tmpl, InstallOptions{
				TargetDir: opts.TargetDir,
				DryRun:    opts.DryRun,
				Force:     true,
			}); err != nil {
				return nil, err
			}
			changes.Updated = append(changes.Updated, "CLAUDE.md")
		} else {
			changes.Skipped = append(changes.Skipped, "CLAUDE.md")
		}
	}

	return changes, nil
}

// GetRulesPath returns the path to the rules file
func (c *ClaudeCodeAdapter) GetRulesPath() string {
	return "CLAUDE.md"
}
